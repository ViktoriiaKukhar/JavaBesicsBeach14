package review.class4;

public class ScannerLoopDemo {

	public static void main(String[] args) {
		

		
		for(int j=0; j<3; j++) {
			
			for(int i=0; i<5; i++) 
			{
				System.out.println(i);
			}
		}
		
		
		for(int j=0; j<3; j++) {
			
			int i=0;
			while (i<3)
			{
				System.out.println(i);
			}
		}
	}

}
